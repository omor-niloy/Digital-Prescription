  Future<List<Medicine>> searchMedicines(String query) async {
    final db = await database;
    if (query.isEmpty) {
      return [];
    }
    final List<Map<String, dynamic>> maps = await db.query(
      'medicines',
      where: 'name LIKE ?',
      whereArgs: ['$query%'],
      limit: 5,
    );
    return List.generate(maps.length, (i) {
      return Medicine.fromMap(maps[i]);
    });
  }
